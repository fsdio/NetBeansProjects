    private void gaji_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gaji_deleteActionPerformed
        // TODO add your handling code here:
        try {
            String SQLQuery = "DELETE FROM `tb_gaji` WHERE `karyawan` = ?;";
            con = (Connection) Connect.configDB();
            pst = con.prepareStatement(SQLQuery);
            pst.setString(1, gaji_karyawan.getText().toLowerCase());
            pst.executeUpdate();
            gaji_read.doClick();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data berhasil gagal dihapus."+e);
        } finally {
            // Close the result set
            if (res != null) {try {res.close();} catch (SQLException e) {}}
            // Close the prepared statement
            if (pst != null) {try {pst.close();} catch (SQLException e) {}}
            // Close the connection
            if (con != null) {try {con.close();} catch (SQLException e) {}}
        }
    }//GEN-LAST:event_gaji_deleteActionPerformed

