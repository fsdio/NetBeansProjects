    private void dk_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dk_createActionPerformed
        // TODO add your handling code here:
        if(dk_nama.getText().equals("")){
            dk_nama.requestFocus();
        }else if(dk_phone.getText().equals("")){
            dk_phone.requestFocus();
        }else if(dk_gender.getSelectedIndex() == 0){
            dk_gender.requestFocus();
        }else if(dk_ktp.getText().equals("")){
            dk_ktp.requestFocus();
        }else if(dk_divisi.getSelectedIndex() == 0){
            dk_divisi.requestFocus();
        }else if(dk_jabatan.getText().equals("")){
            dk_jabatan.requestFocus();
        }else if(dk_password.getText().equals("")){
            dk_password.requestFocus();
        }else if(dk_pola_kerja.getSelectedIndex() == 0){
            dk_pola_kerja.requestFocus();
        }else{
            try {
                String SQLQuery = "INSERT INTO `tb_karyawan` "
                        + "(`nama`, `password`, `gender`, `ktp`, `divisi`, `jabatan`, `phone`, `pola_kerja`) VALUES "
                        + "(?, ?, ?, ?, ?, ?, ?, ?)";
                con = (Connection) Connect.configDB();
                pst = con.prepareStatement(SQLQuery);
                pst.setString(1, dk_nama.getText().toLowerCase());
                pst.setString(2, dk_password.getText().toLowerCase());
                pst.setString(3, String.valueOf(dk_gender.getSelectedItem()).toLowerCase());
                pst.setString(4, dk_ktp.getText().toLowerCase());
                pst.setString(5, String.valueOf(dk_divisi.getSelectedItem()).toLowerCase());
                pst.setString(6, dk_jabatan.getText().toLowerCase());
                pst.setString(7, dk_phone.getText().toLowerCase());
                pst.setString(8, String.valueOf(dk_pola_kerja.getSelectedItem()).toLowerCase());
                pst.execute();
                dk_read.doClick();
                JOptionPane.showMessageDialog(this, "Data berhasil dimasukan!");
            } catch (SQLIntegrityConstraintViolationException e) {
                JOptionPane.showMessageDialog(this, "Data sudah tersedia.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Data gagal dimasukan!" + e);
            } finally {
                // Close the result set
                if (res != null) {try {res.close();} catch (SQLException e) {}}
                // Close the prepared statement
                if (pst != null) {try {pst.close();} catch (SQLException e) {}}
                // Close the connection
                if (con != null) {try {con.close();} catch (SQLException e) {}}
            }
        }
    }//GEN-LAST:event_dk_createActionPerformed

